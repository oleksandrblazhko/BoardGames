"""
Цей скрипт збирає інформацію про карткові ігри з простими правилами з вебсайту desktopgames.com.ua.
Він використовує бібліотеки requests та BeautifulSoup для отримання та парсингу HTML-вмісту сторінок.

Основні етапи роботи скрипта:
1. Ініціалізація: Встановлюється базова URL-адреса для пошуку ігор, початковий номер сторінки та заголовки HTTP-запитів (включаючи User-Agent).
2. Цикл обходу сторінок: Скрипт переходить по сторінках, доки не виявить, що більше немає сторінок для обходу.
3. Отримання контенту: HTML-вміст вебсайту отримується через requests.get() з використанням встановлених заголовків.
4. Парсинг HTML: Використовується BeautifulSoup для аналізу HTML-вмісту і пошук елементів з іграми.
5. Пошук ігор: Скрипт шукає контейнери з іграми, використовуючи різні можливі CSS-класи (item, product, goods, li).
6. Обробка знайдених елементів: Для кожного знайденого контейнера шукаються посилання на ігри, які потім додаються до списку.
7. Очищення даних: З назв ігор видаляються зайві позначки, такі як кількість гравців, вік, кількість відгуків, ціни, тощо.
8. Перевірка дублікатів: Перевіряється, чи вже є гра в списку, щоб уникнути дублювання.
9. Пагінація: Перевіряється наявність наступної сторінки шляхом пошуку посилання з наступним номером сторінки.
10. Фінальна очистка: Виконується додаткова очистка назв ігор та видалення дублікатів.
11. Експорт результатів: Результати зберігаються у форматі Markdown з таблицею, що містить номер, назву гри та посилання на сторінку з описом.

Особливості реалізації:
- Захист від перенавантаження сервера: Використовується time.sleep(1) між запитами.
- Гнучкість селекторів: Скрипт використовує кілька можливих селекторів для пошуку контейнерів ігор.
- Очищення даних: Використовуються регулярні вирази для видалення зайвих елементів з назв ігор.
- Валідація посилань: Перевіряється, чи є посилання дійсними посиланнями на сторінки ігор.
- Відкидання небажаних посилань: Відфільтровуються посилання на статті, контакти, відгуки тощо.

Потенційні проблеми:
- Залежність від структури сайту: Якщо сайт змінить структуру HTML або назви класів, скрипт може перестати працювати.
- Обмеження на стороні сервера: Можливе обмеження доступу або блокування при надто частих запитах.
- Нестабільність виявлення елементів: Використання кількох альтернативних селекторів може призвести до випадкового вилучення неправильних елементів.

Порівняння з попередньою версією:
Цей скрипт використовує бібліотеку requests замість Playwright, що підходить для сайтів, які не використовують динамічне завантаження контенту через JavaScript (як вважається для цього вебсайту).
"""
import requests
from bs4 import BeautifulSoup
import time
import re

# Функція для вилучення інформації про карткові ігри
def extract_card_games():
    # Базова URL-адреса для пошуку ігор з простими правилами та карткових ігор
    base_url = "https://desktopgames.com.ua/ua/catalog/boardgames/prostyie-pravila.kartkovi/?page="
    all_games = [] # Список для зберігання всіх знайдених ігор
    page_num = 1 # Початковий номер сторінки

    # Заголовки HTTP-запиту, включаючи User-Agent, для імітації браузера
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
    }

    # Цикл для обходу сторінок сайту
    # Цикл для обходу сторінок сайту
    while True:
        # Формування URL для поточної сторінки
        url = base_url + str(page_num)
        print(f"Обробка сторінки {page_num}: {url}") # Виведення інформації про поточну сторінку

        try:
            # Виконання HTTP GET-запиту до URL
            response = requests.get(url, headers=headers)
            response.raise_for_status() # Перевірка на наявність помилок HTTP (наприклад, 404, 500)

            # Перевірка, чи отримано дійсний вміст сторінки (за наявністю ключових слів)
            if "Прості правила" in response.text and "Карткові" in response.text:
                # Парсинг HTML-вмісту сторінки за допомогою BeautifulSoup
                soup = BeautifulSoup(response.content, 'html.parser')

                # Пошук контейнерів ігрових елементів за різними можливими CSS-класами
                game_containers = soup.find_all('div', class_='item') or \
                                  soup.find_all('div', class_='product') or \
                                  soup.find_all('div', class_='goods')

                # Якщо контейнери не знайдені за div, спробувати знайти їх як елементи списку (li)
                if not game_containers:
                    game_containers = soup.find_all('li')

                # Обробка кожного знайденого контейнера
                for container in game_containers:
                    # Пошук всіх посилань (тегів 'a') у поточному контейнері
                    links = container.find_all('a', href=True)

                    for link in links:
                        href = link.get('href', '') # Отримання значення атрибута 'href'

                        # Пропуск посилань, які не є посиланнями на ігри (наприклад, статті, контакти)
                        if any(skip in href for skip in ['article', 'contacts', 'buy', 'warranty', 'opt', 'project', 'club']):
                            continue

                        # Отримання текстового вмісту посилання (назви гри)
                        title = link.get_text().strip()

                        # Пропуск посилань, якщо назва занадто коротка, є числом або містить текст, пов'язаний з відгуками
                        if len(title) <= 2 or title.isdigit() or 'відгук' in title.lower():
                            continue

                        # Пропуск, якщо назва схожа на ціну
                        if re.search(r'\d+\s*₴', title):
                            continue

                        # Очищення назви від зайвої інформації (кількість гравців, вік, тощо)
                        # Видалення кількості гравців та вікових маркерів (наприклад, "2-8", "6+")
                        clean_title = re.split(r'\s+\d+[-+]?\d*\s*[+]', title)[0].strip()
                        clean_title = re.split(r'\s+\d+[-+]\d+\s+', clean_title)[0].strip()
                        clean_title = re.split(r'\s+\d+\s+', clean_title)[0].strip()

                        # Додаткове очищення
                        clean_title = re.sub(r'\s*\d+\s*відгук.*$', '', clean_title, flags=re.IGNORECASE) # Видалення тексту про відгуки
                        clean_title = re.sub(r'\s*ТОП\s*$', '', clean_title, flags=re.IGNORECASE) # Видалення "ТОП"
                        clean_title = re.sub(r'\s*ДОПОВНЕННЯ\s*$', '', clean_title, flags=re.IGNORECASE) # Видалення "ДОПОВНЕННЯ"
                        clean_title = re.sub(r'\s*[0-9]+[-+][0-9]*\s*', '', clean_title) # Повторне видалення діапазонів гравців
                        clean_title = re.sub(r'\s*[0-9]+\s*[+]', '', clean_title) # Видалення вікових індикаторів
                        clean_title = re.sub(r'\s*[0-9]+[-+][0-9]+\s*', '', clean_title) # Видалення діапазонів часу

                        # Формування повного посилання, якщо воно відносне
                        if href.startswith('/'):
                            full_link = 'https://desktopgames.com.ua' + href
                        else:
                            full_link = href

                        # Перевірка, чи є посилання сторінкою гри та чи має назва сенс
                        if '.html' in full_link and len(clean_title) > 2 and not clean_title.startswith('095'):
                            # Перевірка на дублікати
                            game_exists = any(game['link'] == full_link for game in all_games)
                            if not game_exists and clean_title and len(clean_title) > 2:
                                all_games.append({
                                    'title': clean_title,
                                    'link': full_link
                                })

                print(f"Знайдено ігор на сторінці {page_num}, всього зібрано ігор: {len(all_games)}")

                # Затримка перед наступним запитом, щоб уникнути перевантаження сервера
                time.sleep(1)

                # Перевірка наявності наступної сторінки (пошук посилання з наступним номером сторінки)
                pagination_links = soup.find_all('a', string=str(page_num + 1))

                if pagination_links:
                    page_num += 1 # Перехід до наступної сторінки
                else:
                    print("Більше сторінок не знайдено, зупинка.")
                    break # Вихід з циклу, якщо наступної сторінки немає

            else:
                print(f"Більше сторінок для обробки після сторінки {page_num} не знайдено, зупинка.")
                break # Вихід з циклу, якщо вміст сторінки не відповідає очікуванням

        except requests.RequestException as e:
            print(f"Помилка при отриманні сторінки {page_num}: {e}")
            break # Вихід з циклу у випадку помилки запиту
    
    # Видалення дублікатів та фінальне очищення списку ігор
    cleaned_games = [] # Список для зберігання унікальних ігор
    seen_links = set() # Множина для відстеження вже доданих посилань

    # Прохід по всіх зібраних іграх для видалення дублікатів
    for game in all_games:
        if game['link'] not in seen_links:
            cleaned_games.append(game)
            seen_links.add(game['link'])

    # Фінальний прохід очищення - видалення будь-яких залишків артефактів з назв
    final_games = [] # Список для зберігання остаточно очищених ігор
    for game in cleaned_games:
        # Фінальне очищення назви
        title = game['title']

        # Видалення будь-яких чисел або спеціальних символів, що залишилися в кінці назви
        title = re.split(r'\s+\d+[-+]\d*', title)[0].strip()
        title = re.split(r'\s+\d+[+]', title)[0].strip()

        # Видалення спеціальних символів, які могли залишитися
        title = re.sub(r'[0-9+]+$', '', title).strip()
        title = re.sub(r'\s*[-+]$', '', title).strip()

        if title and len(title) > 2: # Додавати гру, тільки якщо назва є значущою
            final_games.append({
                'title': title,
                'link': game['link']
            })
    
    # Збереження результатів у Markdown файл
    with open('GameList-desktopgames.md', 'w', encoding='utf-8') as f:
        f.write("# Список карткових ігор із простими правилами з desktopgames.com.ua\n\n")
        f.write("| N | Назва гри | Посилання на сторінку з описом гри |\n")
        f.write("|---|-----------|------------------------|\n")

        # Запис кожної гри в таблицю Markdown
        for i, game in enumerate(final_games, 1):
            # Очищення назви від зайвих пробілів та переносів рядків
            clean_title = re.sub(r'\s+', ' ', game['title']).strip()
            f.write(f"| {i} | {clean_title} | {game['link']} |\n")

    print(f"\nВсього знайдено унікальних ігор: {len(final_games)}")
    print("Результати збережено у файл GameList-desktopgames.md")

# Основний блок виконання скрипта
if __name__ == "__main__":
    extract_card_games() # Виклик функції для вилучення ігор